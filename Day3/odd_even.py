# Modulo is %
# 7 % 2 = 2+2+2+ 1  => 1
# 7 % 3 = 3+3+ 1 =>1
print("Welcome to Even-odd number!")
input_number = int(input("Enter the number: "))
input_number > 0
if input_number % 2 == 0:
    print(f"Given number {input_number} is EVEN number")
else:
    print(f"Given number {input_number} is ODD number")